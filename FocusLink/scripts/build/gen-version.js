// 生成版本信息到 shared/version.generated.ts
// 供主进程日志和渲染进程 UI 共用，build/dist 前自动执行
const { execSync } = require('node:child_process');
const fs = require('node:fs');
const path = require('node:path');

// 从 package.json 读取版本，避免多处硬编码不同步
const root = path.resolve(__dirname, '..', '..');
const pkg = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'));
const APP_VERSION = pkg.version;
const APP_RELEASE_DIR = `release-v${APP_VERSION.replace(/\./g, '')}`;

let commit = 'unknown';
try {
  commit = execSync('git rev-parse --short HEAD').toString().trim();
  const dirtyEntries = execSync('git status --porcelain --untracked-files=normal', { cwd: root })
    .toString()
    .split(/\r?\n/)
    .filter(Boolean)
    // The generator rewrites this tracked file on every build. It must not make an
    // otherwise clean source commit report itself as dirty on the next build. Git may
    // report the path from the repository root even when cwd points at FocusLink.
    .filter((line) => !line.replace(/\\/g, '/').endsWith('shared/version.generated.ts'));
  if (dirtyEntries.length > 0) commit += '-dirty';
} catch {
  commit = 'dev-' + Date.now().toString(36);
}

const buildTime = new Date().toISOString();
const quoteTs = (value) => `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;

const content = `// AUTO-GENERATED by scripts/build/gen-version.js — 请勿手动编辑
export const APP_COMMIT = ${quoteTs(commit)};
export const APP_BUILD_TIME = ${quoteTs(buildTime)};
`;

const outPath = path.join(root, 'shared', 'version.generated.ts');
fs.writeFileSync(outPath, content, 'utf8');
console.log(
  `[gen-version] version=${APP_VERSION} commit=${commit} buildTime=${buildTime} -> ${outPath}`,
);
